package com.myblog.blog.entry.somemodel;

public class UserTable {
    private Integer uid;

    private String name;

    private String six;

    private String like;

    private Integer loveId;

    private Integer acticleId;

    private Integer commentId;

    public Integer getUid() {
        return uid;
    }

    public void setUid(Integer uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSix() {
        return six;
    }

    public void setSix(String six) {
        this.six = six;
    }

    public String getLike() {
        return like;
    }

    public void setLike(String like) {
        this.like = like;
    }

    public Integer getLoveId() {
        return loveId;
    }

    public void setLoveId(Integer loveId) {
        this.loveId = loveId;
    }

    public Integer getActicleId() {
        return acticleId;
    }

    public void setActicleId(Integer acticleId) {
        this.acticleId = acticleId;
    }

    public Integer getCommentId() {
        return commentId;
    }

    public void setCommentId(Integer commentId) {
        this.commentId = commentId;
    }
}